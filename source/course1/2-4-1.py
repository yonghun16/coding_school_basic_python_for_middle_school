# 2-4-1 : 자료형 확인하기

abc = "abc"       # 변수abc에 문자열 자료 "abc"이 할당됨
a = "abc"         # 변수a에 문자열 자료 "abc"이 할당됨
b = 123           # 변수b에 숫자 123이 할당됨
c = "123"         # 변수c에 문자열 자료 "123"이 할당됨
d = abc           # 변수d에 변수abc가 할당됨

print(type(a))    # <class 'str'>  'str'은 문자열
print(type(b))    # <class 'int'>  'int'은 숫자
print(type(c))    # <class 'str'>  'str'은 문자열
print(type(d))    # <class 'str'>  'str'은 문자열


